//配列-変数の集合体-
#include<stdio.h>

int main(void){

	int a[5];/*型 配列名 [要素数]と宣言する
下記のような宣言も可能
	int a =5;
	int a[a];
*/

	a[1]= 0;
/*配列の何番目にアクセスするかを[]に書く
これを添え字という*/

	printf("配列2番目の値は%dです\n",a[1]);//配列は0からなので実質2番めになります

//多次元配列
	int b[2][2];//この場合２次元配列と呼ばれる。[]の数が次元の数

	//アクセス
	b[0][0] = 0;
	b[0][1] = 1;
	b[1][0] = 2;
	b[1][1] = 3;
	puts("多次元配列");
	printf("%d%d\n",b[0][0],b[0][1] );
	printf("%d%d\n",b[1][0],b[1][1] );

return 0;
}
/*実行結果 配列2番目の値は0ですと表示される
		  多次元配列
		  01
		  23
		  と表示される

 添字には定数の他に変数を入れられます。そのためb + 1の配列の値を表示するなども可
 また通常の変数と同じで一気にアクセスできるまた上記のプログラムで5にアクセスした場合
　配列オーバーとなるがコンパイルエラーにはならないので気をつけること!!

 ポインタと配列の関係はArray_Pointer.cを参照
  */
